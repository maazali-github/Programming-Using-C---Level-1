#include <iostream>
using namespace std;

int main()
{
    short int a, b;
    cout << "Enter the first number a: ";
    cin >> a;
    cout << "Enter the first number b: ";
    cin >> b;

    cout << "a + b = " << a + b << endl;
    cout << "a - b = " << a - b << endl;
    cout << "a * b = " << a * b << endl;
    cout << "a / b = " << a / b << endl;
    cout << "a % b = " << a % b << endl;

}
